#!/usr/bin/python
#lista

lista =[10,7,3,7,2]
suma = 0
nro_elementos = 0
x = 0

while x<len(lista):
	suma = suma + lista[x]
	nro_elementos = nro_elementos + 1
	x=x+1
print("Los elementos de la lista son: " + str(lista))
print("La suma de los elementos de la lista es: " + str(suma))
print("la cantidad de elementos de la lista es: " + str(nro_elementos))
print("")



